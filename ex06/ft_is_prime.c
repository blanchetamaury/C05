int	ft_is_prime(int nb)
{
	int	i;
	int	a;

	if (nb == 0 || nb == 1 || nb < 0)
		return (0);
	i = 2;
	while (i < nb)
	{
		a = nb % i;
		if (a == 0)
			return (0);
		++i;
	}
	return (1);
}
